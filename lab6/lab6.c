#include <stdio.h>

int main (void)
{
    char ch;           // Текущий символ
    int e = 0;         // Флаг (1 если предыдущий символ был 'e')
    int count = 0;     // Счётчик сочетаний "ei"

    printf("Введите текст с сочетаниями ei (# для выхода)\n");

    // Читаем символы пока не встретим '#'
    while ((ch = getchar()) != '#')
    {
        if (ch == 'e')          // Если текущий символ 'e'
            e = 1;              // Устанавливаем флаг
        else if (ch == 'i' && e == 1)  // Если символ 'i' и предыдущий был 'e'
        {
            count++;            // Увеличиваем счётчик сочетаний "ei"
            e = 0;              // Сбрасываем флаг
        }
        else                    // Для всех других символов
            e = 0;              // Сбрасываем флаг
    }
    
    printf("Было найдено %d сочетаний 'ei'", count);

    return 0;
}