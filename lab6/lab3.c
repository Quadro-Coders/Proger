#include <stdio.h>

int main (void)
{
    int i;                    // Вводимое число
    int even = 0;             // Счётчик чётных чисел
    int sum_even = 0;         // Сумма чётных чисел
    int odd = 0;              // Счётчик нечётных чисел
    int sum_odd = 0;          // Сумма нечётных чисел
    float average_even;       // Среднее чётных чисел
    float average_odd;        // Среднее нечётных чисел

    printf("Введите целые числа (0 для выхода)..\n");

    // Читаем числа пока успешно считывается и число не равно 0
    while (scanf("%d", &i) == 1 && i != 0)
    {
        if ((i % 2) == 0)     // Если число чётное
        {
            even++;           // Увеличиваем счётчик чётных
            sum_even += i;    // Добавляем к сумме чётных
        }
        else                  // Если число нечётное
        {
            odd++;            // Увеличиваем счётчик нечётных
            sum_odd += i;     // Добавляем к сумме нечётных
        }
    }
    
    // Выводим результаты для чётных чисел, если они были
    if (even > 0)
    {
        average_even = sum_even / (float)even;  // Вычисляем среднее
        printf("Чётных чисел: %d (среднее %.0f)\n", 
               even, average_even);
    }
    
    // Выводим результаты для нечётных чисел, если они были
    if (odd > 0)
    {
        average_odd = sum_odd / (float)odd;     // Вычисляем среднее
        printf("Нечётных чисел: %d (среднее %.0f)", 
               odd, average_odd);
    }
    
    return 0;
}